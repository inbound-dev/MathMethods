package showwhatyouknow1;
import java.util.*;
/**
 *
 * @author joham3169
 */
public class ShowWhatYouKnow1 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       boolean Mainloop = true;
       int NUM_COUNTER;
       NUM_COUNTER = 10; 
       
       while (Mainloop == true)
       {
           for (int i = NUM_COUNTER; i>= 0; i--)
           {
               System.out.println(i);
           }
       }
    }
    
}
